#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen(s) - 1;
	if (!s || (!*(char *)s && c))
		return (0);
	if (!c)
		return ((char *)s + i + 1);
	while (s[i] != c)
	{
		if (!i--)
			return (0);
	}
	return ((char *)s + i);
}
