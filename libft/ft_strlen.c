#include "libft.h"

/*
** ft_strlen
** return the length of the given string
** -
** takes a strings
** return it's length as a size_t
*/

size_t	ft_strlen(const char *s)
{
	size_t t;

	t = 0;
	while (s[t])
		t++;
	return (t);
}
