#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*r;
	int		i1;
	int		i2;

	i1 = 0;
	i2 = 0;
	if (!s)
		return (0);
	if (!(r = ft_strnew(ft_strlen(s))))
		return (0);
	while (s[i2])
		r[i1++] = f(s[i2++]);
	return (r);
}
