#include "libft.h"
#include <stdlib.h>

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char *ptr;

	if (!s)
		return (0);
	if (!(ptr = (char *)malloc(len + 1)))
		return (0);
	ft_strncpy(ptr, s + start, len);
	ptr[len] = 0;
	return (ptr);
}
