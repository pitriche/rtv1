#include "rtv1.h"

void	mult_v3(t_v3 *v, double x)
{
	v->x *= x;
	v->y *= x;
	v->z *= x;
}

void	add_v3(t_v3 *v, t_v3 v1, t_v3 v2)
{
	v->x = v1.x + v2.x;
	v->y = v1.y + v2.y;
	v->z = v1.z + v2.z;
}
